'use strict';

/**
 * @ngdoc overview
 * @name appApp
 * @description
 * # appApp
 *
 * Main module of the application.
 */
angular
    .module('appApp', [
        'ngResource',
        'ngTouch',
        'ngRoute',
        'ionic'
    ])
    .config(function ($routeProvider) {
        $routeProvider
            .when('/', {
                templateUrl: 'views/main.html',
                controller: 'MainCtrl'
            })
            .when('/camera', {
                templateUrl: 'views/camera.html',
                controller: 'CameraCtrl'
            })
            .when('/vibration', {
                templateUrl: 'views/vibration.html',
                controller: 'VibrationCtrl'
            })
            .when('/about', {
                templateUrl: 'views/about.html',
                controller: 'AboutCtrl'
            })
            .otherwise({
                redirectTo: '/'
            });
    });

